/**
 * Generate page header with title and logo
 * @param {string} pageTitle - Title for the page header
 * @returns {string} - HTML for page header
 */
export function generatePageHeader(pageTitle) {
  // Split "LOT X - Title" to color the parts differently
  const lotMatch = pageTitle.match(/^(LOT\s+\d+\s+-\s+)(.+)$/)
  const formattedTitle = lotMatch
    ? `<span style="color: #8E8E8E;">${lotMatch[1]}</span>${lotMatch[2]}`
    : pageTitle

  return `
    <div class="page-header">
      <div class="page-title">${formattedTitle}</div>
      <hr class="page-header-separator" />
      <div class="page-logo">${CROWN_LOGO_SVG}</div>
    </div>
  `
}
